                        Le nom « Cheval de Troie » provient d'une légende narrée dans l'Iliade (de l'écrivain Homère) à propos du siège de la ville de Troie par les Grecs.
                        La légende veut que les Grecs, n'arrivant pas à pénétrer dans les fortifications de la ville,
                        eurent l'idée de donner en cadeau un énorme cheval de bois en offrande à la ville en abandonnant le siège.
                        Les troyens (peuple de la ville de Troie), apprécièrent cette offrande à priori inoffensive et la ramenèrent dans les murs de la ville.
                        Cependant le cheval était rempli de soldats cachés qui s'empressèrent d'en sortir à la tombée de la nuit, alors que la ville entière était endormie,
                        pour ouvrir les portes de la cité et en donner l'accès au reste de l'armée ...
                        Un cheval de Troie (informatique) est donc un programme caché dans un autre qui exécute des commandes sournoises,
